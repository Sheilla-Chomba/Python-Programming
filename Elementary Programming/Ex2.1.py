# Below is the programming exercise
# (Convert Celsius to Fahrenheit) Write a program that reads a Celsius degree from
# the console and converts it to Fahrenheit and displays the result.

# Prompts user to type the level of temperature in Celcius Degree
celcius=int(input("Enter the temperature in Celcius Degree: "))

# Introduction of a new variable Fahrenheit and the formula to get it
fahrenheit=(9/5)*celcius+32

# Prints out the value of the temperature in Fahrenheit
print("The temperature in Fahrenheit is ",fahrenheit)